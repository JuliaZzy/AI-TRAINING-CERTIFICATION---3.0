def _single_textarea(label: str) -> dict:
    return {
        "layout": "sections",
        "sections": [
            {"type": "textarea", "label": label, "large": True, "placeholder": "请在此填写"},
        ],
    }


def _line_section(line: str) -> dict:
    if BLANK_MARK not in line:
        return {"type": "text_line", "text": line}
    parts = line.split(BLANK_MARK)
    segments: list[dict] = []
    for i, part in enumerate(parts):
        if part:
            segments.append({"type": "text", "value": part})
        if i < len(parts) - 1:
            segments.append({"type": "blank"})
    return {"type": "fill_line", "segments": segments}


def _outline_doc(main_title: str, blocks: list[tuple[str, list[str]]]) -> dict:
    sections: list[dict] = [{"type": "subheading", "text": main_title}]
    for title, lines in blocks:
        sections.append({"type": "block_title", "text": title})
        for line in lines:
            sections.append(_line_section(line))
    return {"layout": "sections", "sections": sections}


def _guide_doc(main_title: str, lines: list[str]) -> dict:
    """指导方案类文档：编号小节 + 条目，【下划线】为填空。"""
    sections: list[dict] = [{"type": "subheading", "text": main_title}]
    for line in lines:
        if re.match(r"^\d+\.", line):
            sections.append({"type": "block_title", "text": line})
        else:
            sections.append(_line_section(line))
    return {"layout": "sections", "sections": sections}


