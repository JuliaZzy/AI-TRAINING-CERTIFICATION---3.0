from __future__ import annotations

import re


def _two_textareas(labels: list[str]) -> dict:
    return {
        "layout": "sections",
        "sections": [
            {"type": "textarea", "label": label, "large": True, "placeholder": "请在此填写"}
            for label in labels
        ],
    }


def _model_report(model: str, table: dict) -> dict:
    return {
        "layout": "sections",
        "sections": [
            {"type": "heading", "text": f"针对{model}"},
            {"type": "subheading", "text": "1、模型性能"},
            {"type": "table", **table},
            {"type": "textarea", "label": "2、错误分析", "large": True, "placeholder": "请填写错误分析"},
            {"type": "textarea", "label": "3、改进建议", "large": True, "placeholder": "请填写改进建议"},
        ],
    }


def _label_value_table(rows: list[str]) -> dict:
    return {
        "headers": None,
        "rows": [{"label": label, "cells": 1} for label in rows],
    }


def _kv_rows(pairs: list[tuple[str, int]]) -> list[dict]:
    return [{"label": label, "cells": cells} for label, cells in pairs]


def _optimization_sections() -> list[dict]:
    sections: list[dict] = [{"type": "subheading", "text": "2、优化方向及解决方案"}]
    for i in range(1, 4):
        sections.extend([
            {"type": "textarea", "label": f"优化方向{i}：", "large": True, "placeholder": "请在此填写"},
            {"type": "textarea", "label": f"对应解决方案{i}：", "large": True, "placeholder": "请在此填写"},
        ])
    return sections


def _grid_table(group_title: str, headers: list[str], row_labels: list[str]) -> list[dict]:
    return [
        {"type": "group_title", "text": group_title},
        {"type": "grid_table", "headers": headers, "rowLabels": row_labels},
    ]


def _kv_group(group_title: str, rows: list[tuple[str, int]]) -> list[dict]:
    return [
        {"type": "group_title", "text": group_title},
        {"type": "table", "headers": None, "rows": _kv_rows(rows)},
    ]


